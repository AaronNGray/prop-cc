#include <AD/symbolic/expr.h>

///////////////////////////////////////////////////////////////////////////////
//
//  Default constructor and destructor.
//
///////////////////////////////////////////////////////////////////////////////

SymbolicExpr:: SymbolicExpr() {}
SymbolicExpr::~SymbolicExpr() {}

///////////////////////////////////////////////////////////////////////////////
//
//  Default implementation of eval and copy.
//
///////////////////////////////////////////////////////////////////////////////

SymbolicExpr * SymbolicExpr::eval (SymbolicEnvironment&) const
{ return (SymbolicExpr *)this; }

SymbolicExpr * SymbolicExpr::copy () const
{ return (SymbolicExpr *)this; }
